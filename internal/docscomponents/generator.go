type dependencyRow struct {
	id   string
	link string
	deps []string
}

type typedRecord struct {
	id        string
	title     string
	typeLabel string
	deps      []string
}

type recordWithPath[T any] struct {
	sourcePath string
	data       T
}

func (g *Generator) loadRoadmapArtifacts() (roadmapArtifacts, error) {
	var result roadmapArtifacts

	milestones, err := g.readArtifactRecords(filepath.Join(g.repoRoot, "@hubless", "roadmap", "milestones"))
	if err != nil {
		return result, fmt.Errorf("load milestones: %w", err)
	}

	features, err := g.readArtifactRecords(filepath.Join(g.repoRoot, "@hubless", "roadmap", "features"))
	if err != nil {
		return result, fmt.Errorf("load features: %w", err)
	}

	stories, err := g.readArtifactRecords(filepath.Join(g.repoRoot, "@hubless", "issues", "stories"))
	if err != nil {
		return result, fmt.Errorf("load stories: %w", err)
	}

	result.Milestones = milestones
	result.Features = features
	result.Stories = stories

	return result, nil
}

func (g *Generator) readArtifactRecords(dir string) ([]recordWithPath[artifactRecord], error) {
	if _, err := os.Stat(dir); err != nil {
		if errors.Is(err, os.ErrNotExist) {
			return nil, nil
		}
		return nil, err
	}

	files, err := filepath.Glob(filepath.Join(dir, "*.json"))
	if err != nil {
		return nil, fmt.Errorf("glob %s: %w", dir, err)
	}

	if len(files) == 0 {
		return nil, nil
	}

	sort.Strings(files)

	results := make([]recordWithPath[artifactRecord], 0, len(files))
	for _, file := range files {
		data, err := os.ReadFile(file)
		if err != nil {
			return nil, fmt.Errorf("read %s: %w", file, err)
		}

		var record artifactRecord
		if err := json.Unmarshal(data, &record); err != nil {
			return nil, fmt.Errorf("decode %s: %w", file, err)
		}

		if record.ID == "" || record.Title == "" || record.Status == "" {
			return nil, fmt.Errorf("record %s missing required fields", file)
		}

		results = append(results, recordWithPath[artifactRecord]{sourcePath: file, data: record})
	}

	return results, nil
}

