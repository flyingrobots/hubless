const generatedNotice = "<!-- Generated by docs-components; do not edit manually. -->"

// Generator creates reusable Markdown snippets from structured planning data.
type GeneratorOptions struct {
	GraphDirection string
	GraphClusters  bool
	GraphPalette   string
	PaletteFile    string
}

type Generator struct {
	repoRoot      string
	componentsDir string
	options       GeneratorOptions
	palettes      map[string]map[string]paletteColor
}

// NewGenerator creates a Generator configured to write component snippets for the repository at
// repoRoot. It resolves and stores absolute paths for repoRoot and componentsDir (defaulting
// componentsDir to repoRoot/docs/components when empty), ensures the components directory exists,
// loads the built-in palettes and merges palettes from options.PaletteFile if provided, and
// normalizes the supplied GeneratorOptions. Returns an initialized Generator or an error if input
// validation, path resolution, directory creation, or palette loading fails.
func NewGenerator(repoRoot, componentsDir string, options GeneratorOptions) (*Generator, error) {
	if repoRoot == "" {
		return nil, errors.New("repoRoot is required")
	}

	absRepoRoot, err := filepath.Abs(repoRoot)
	if err != nil {
		return nil, fmt.Errorf("resolve repo root: %w", err)
	}

	absComponentsDir := componentsDir
	if absComponentsDir == "" {
		absComponentsDir = filepath.Join(absRepoRoot, "docs", "components")
	}
	absComponentsDir, err = filepath.Abs(absComponentsDir)
	if err != nil {
		return nil, fmt.Errorf("resolve components dir: %w", err)
	}

	if err := os.MkdirAll(absComponentsDir, 0o755); err != nil {
		return nil, fmt.Errorf("ensure components dir: %w", err)
	}

	palettes := defaultPalettes()
	if options.PaletteFile != "" {
		palettePath := options.PaletteFile
		if !filepath.IsAbs(palettePath) {
			palettePath = filepath.Join(absRepoRoot, palettePath)
		}
		if err := mergePaletteFile(palettePath, palettes); err != nil {
			return nil, fmt.Errorf("load palette file: %w", err)
		}
	}

	options = normalizeOptions(options, palettes)

	return &Generator{repoRoot: absRepoRoot, componentsDir: absComponentsDir, options: options, palettes: palettes}, nil
}

// RepoRoot exposes the absolute repository root path used by the generator.
func (g *Generator) RepoRoot() string {
	return g.repoRoot
}

// ComponentsDir exposes the absolute components directory path.
func (g *Generator) ComponentsDir() string {
	return g.componentsDir
}

// Generate produces the default set of Markdown snippets.
func (g *Generator) Generate(ctx context.Context) error {
	roadmap, err := g.loadRoadmapArtifacts()
	if err != nil {
		return err
	}

	tasks, err := g.readTaskRecords(filepath.Join(g.repoRoot, "@hubless", "issues", "tasks"))
	if err != nil {
		return fmt.Errorf("load task records: %w", err)
	}

	if err := g.generateRoadmapTables(ctx, roadmap); err != nil {
		return err
	}

	if err := g.generateProgressOverview(ctx, roadmap, tasks); err != nil {
		return err
	}

	if err := g.generateDependencySummary(ctx, roadmap, tasks); err != nil {
		return err
	}

	if err := g.generateDependencyGraph(ctx, roadmap, tasks); err != nil {
		return err
	}

	if err := g.generateTaskTable(ctx, tasks); err != nil {
		return err
	}

	if err := g.generateTaskStatusSummary(ctx, tasks); err != nil {
		return err
	}

	if err := g.generateArchivedStories(ctx, roadmap); err != nil {
		return err
	}

	if err := g.generateArchivedTasks(ctx, tasks); err != nil {
		return err
	}

	if err := g.generateChangelog(ctx, tasks); err != nil {
		return err
	}

	return nil
}

type artifactRecord struct {
	ID           string   `json:"id"`
	Title        string   `json:"title"`
	Status       string   `json:"status"`
	Dependencies []string `json:"dependencies"`
}

type taskRecord struct {
	ID           string   `json:"id"`
	Title        string   `json:"title"`
	Status       string   `json:"status"`
	Owner        *string  `json:"owner"`
	Labels       []string `json:"labels"`
	Badges       []string `json:"badges"`
	UpdatedAt    *string  `json:"updated_at"`
	Dependencies []string `json:"dependencies"`
}

type roadmapArtifacts struct {
	Milestones []recordWithPath[artifactRecord]
	Features   []recordWithPath[artifactRecord]
	Stories    []recordWithPath[artifactRecord]
}

func (g *Generator) generateRoadmapTables(ctx context.Context, data roadmapArtifacts) error {
	tables := []struct {
		name           string
		records        []recordWithPath[artifactRecord]
		linkPrefix     string
		outputFilename string
	}{
		{
			name:           "milestones",
			records:        data.Milestones,
			linkPrefix:     "../milestones",
			outputFilename: "milestones-table.md",
		},
		{
			name:           "features",
			records:        data.Features,
			linkPrefix:     "../features",
			outputFilename: "features-table.md",
		},
		{
			name:           "stories",
			records:        data.Stories,
			linkPrefix:     "../../issues/stories",
			outputFilename: "stories-table.md",
		},
	}

	for _, table := range tables {
		select {
		case <-ctx.Done():
			return ctx.Err()
		default:
		}

		builder := &strings.Builder{}
		builder.WriteString(generatedNotice)
		builder.WriteString("\n")
		builder.WriteString("| ID | Title | Status |\n")
		builder.WriteString("|----|-------|--------|\n")

		for _, record := range table.records {
			link := path.Join(table.linkPrefix, filepath.Base(record.sourcePath))
			fmt.Fprintf(builder, "| [%s](%s) | %s | %s |\n", record.data.ID, link, record.data.Title, record.data.Status)
		}

		outputPath := filepath.Join(g.componentsDir, "roadmap", table.outputFilename)
		if err := g.writeFile(outputPath, builder.String()); err != nil {
			return fmt.Errorf("write %s table: %w", table.name, err)
		}
	}

	return nil
}

func (g *Generator) generateProgressOverview(ctx context.Context, data roadmapArtifacts, tasks []recordWithPath[taskRecord]) error {
	select {
	case <-ctx.Done():
		return ctx.Err()
	default:
	}

	builder := &strings.Builder{}
	builder.WriteString(generatedNotice)
	builder.WriteString("\n")
	builder.WriteString("| Artifact | Progress | Done | Total |\n")
	builder.WriteString("| --- | --- | --- | --- |\n")

	entries := []struct {
		label string
		done  int
		total int
	}{
		{
			label: "Milestones",
			done:  countDoneArtifacts(data.Milestones),
			total: len(data.Milestones),
		},
		{
			label: "Features",
			done:  countDoneArtifacts(data.Features),
			total: len(data.Features),
		},
		{
			label: "Stories",
			done:  countDoneArtifacts(data.Stories),
			total: len(data.Stories),
		},
		{
			label: "Tasks",
			done:  countDoneTasks(tasks),
			total: len(tasks),
		},
	}

	for _, entry := range entries {
		progress := renderProgressBar(entry.done, entry.total)
		fmt.Fprintf(builder, "| %s | %s | %d | %d |\n", entry.label, progress, entry.done, entry.total)
	}

	outputPath := filepath.Join(g.componentsDir, "roadmap", "progress.md")
	if err := g.writeFile(outputPath, builder.String()); err != nil {
		return fmt.Errorf("write roadmap progress: %w", err)
	}

	return nil
}

func (g *Generator) generateDependencySummary(ctx context.Context, data roadmapArtifacts, tasks []recordWithPath[taskRecord]) error {
	select {
	case <-ctx.Done():
		return ctx.Err()
	default:
	}

	sections := []struct {
		heading string
		rows    []dependencyRow
	}{
		{
			heading: "Milestones",
			rows:    dependencyRowsFromArtifacts(data.Milestones, "../milestones"),
		},
		{
			heading: "Features",
			rows:    dependencyRowsFromArtifacts(data.Features, "../features"),
		},
		{
			heading: "Stories",
			rows:    dependencyRowsFromArtifacts(data.Stories, "../../issues/stories"),
		},
		{
			heading: "Tasks",
			rows:    dependencyRowsFromTasks(tasks, "../../issues/tasks"),
		},
	}

	builder := &strings.Builder{}
	builder.WriteString(generatedNotice)
	builder.WriteString("\n")

	for _, section := range sections {
		builder.WriteString("### " + section.heading + "\n\n")
		builder.WriteString("| ID | Depends On |\n")
		builder.WriteString("|----|-------------|\n")

		if len(section.rows) == 0 {
			builder.WriteString("| — | None recorded |\n\n")
			continue
		}

		for _, row := range section.rows {
			fmt.Fprintf(builder, "| [%s](%s) | %s |\n", row.id, row.link, formatList(row.deps))
		}
		builder.WriteString("\n")
	}

	outputPath := filepath.Join(g.componentsDir, "roadmap", "dependencies.md")
	if err := g.writeFile(outputPath, builder.String()); err != nil {
		return fmt.Errorf("write dependencies summary: %w", err)
	}

	return nil
}

func (g *Generator) generateDependencyGraph(ctx context.Context, data roadmapArtifacts, tasks []recordWithPath[taskRecord]) error {
	select {
	case <-ctx.Done():
		return ctx.Err()
	default:
	}

	var all []typedRecord

	for _, record := range data.Milestones {
		all = append(all, typedRecord{
			id:        record.data.ID,
			title:     record.data.Title,
			typeLabel: "Milestone",
			deps:      cleanValues(record.data.Dependencies),
		})
	}
	for _, record := range data.Features {
		all = append(all, typedRecord{
			id:        record.data.ID,
			title:     record.data.Title,
			typeLabel: "Feature",
			deps:      cleanValues(record.data.Dependencies),
		})
	}
	for _, record := range data.Stories {
		all = append(all, typedRecord{
			id:        record.data.ID,
			title:     record.data.Title,
			typeLabel: "Story",
			deps:      cleanValues(record.data.Dependencies),
		})
	}
	for _, record := range tasks {
		all = append(all, typedRecord{
			id:        record.data.ID,
			title:     record.data.Title,
			typeLabel: "Task",
			deps:      cleanValues(record.data.Dependencies),
		})
	}

	nodeIDs := map[string]string{}
	labels := map[string]string{}
	seenIDs := map[string]string{}

	for idx, record := range all {
		if record.id == "" {
			continue
		}
		if previousType, ok := seenIDs[record.id]; ok {
			return fmt.Errorf("duplicate dependency graph id %q in %s and %s records", record.id, previousType, record.typeLabel)
		}
		seenIDs[record.id] = record.typeLabel
		nodeName := fmt.Sprintf("n%d", idx)
		nodeIDs[record.id] = nodeName
		labels[nodeName] = escapeMermaidLabel(fmt.Sprintf("%s\\n%s", record.typeLabel, record.title))
	}

	edges := make([][2]string, 0)
	for _, record := range all {
		src, ok := nodeIDs[record.id]
		if !ok {
			continue
		}
		for _, dep := range record.deps {
			if dst, ok := nodeIDs[dep]; ok {
				edges = append(edges, [2]string{src, dst})
			}
		}
	}

	direction := strings.ToUpper(strings.TrimSpace(g.options.GraphDirection))
	builder := &strings.Builder{}
	builder.WriteString(generatedNotice)
	builder.WriteString("\n")
	builder.WriteString("```mermaid\n")
	builder.WriteString(fmt.Sprintf("graph %s\n", direction))

	nodeNames := make([]string, 0, len(labels))
	for node := range labels {
		nodeNames = append(nodeNames, node)
	}
	sort.Strings(nodeNames)

	for _, node := range nodeNames {
		label := labels[node]
		fmt.Fprintf(builder, "    %s[\"%s\"]\n", node, label)
	}

	palette := g.palettes[g.options.GraphPalette]
	if palette == nil {
		palette = g.palettes["evergreen"]
	}
	classNames := make([]string, 0, len(palette))
	for className := range palette {
		classNames = append(classNames, className)
	}
	sort.Strings(classNames)
	for _, className := range classNames {
		color := palette[className]
		fmt.Fprintf(builder, "    classDef %s fill:%s,stroke:%s,color:%s,stroke-width:1px;\n", className, color.Fill, color.Stroke, color.Text)
	}

	if g.options.GraphClusters {
		for _, cluster := range orderedTypes {
			nodes := collectNodesByType(cluster, all, nodeIDs)
			if len(nodes) == 0 {
				continue
			}
			fmt.Fprintf(builder, "    subgraph %s\n", cluster)
			fmt.Fprintf(builder, "        direction %s\n", direction)
			for _, node := range nodes {
				fmt.Fprintf(builder, "        %s\n", node)
			}
			builder.WriteString("    end\n")
		}
	}

	classAssignments := make(map[string]string)
	for _, record := range all {
		if node, ok := nodeIDs[record.id]; ok {
			className := strings.ToLower(record.typeLabel)
			if _, ok := palette[className]; ok {
				classAssignments[node] = className
			}
		}
	}

	assignmentNodes := make([]string, 0, len(classAssignments))
	for node := range classAssignments {
		assignmentNodes = append(assignmentNodes, node)
	}
	sort.Strings(assignmentNodes)
	for _, node := range assignmentNodes {
		fmt.Fprintf(builder, "    class %s %s;\n", node, classAssignments[node])
	}

	for _, edge := range edges {
		fmt.Fprintf(builder, "    %s --> %s\n", edge[0], edge[1])
	}

	builder.WriteString("```\n")

	outputPath := filepath.Join(g.componentsDir, "roadmap", "dependencies-graph.md")
	if err := g.writeFile(outputPath, builder.String()); err != nil {
		return fmt.Errorf("write dependency graph: %w", err)
	}

	return nil
}

func (g *Generator) generateTaskTable(ctx context.Context, records []recordWithPath[taskRecord]) error {
	select {
	case <-ctx.Done():
		return ctx.Err()
	default:
	}

	builder := &strings.Builder{}
	builder.WriteString(generatedNotice)
	builder.WriteString("\n")
	builder.WriteString("| ID | Title | Status | Owner | Labels | Badges | Updated |\n")
	builder.WriteString("| --- | --- | --- | --- | --- | --- | --- |\n")

	for _, record := range records {
		owner := "_unassigned_"
		if record.data.Owner != nil && strings.TrimSpace(*record.data.Owner) != "" {
			owner = *record.data.Owner
		}

		labels := formatList(record.data.Labels)
		badges := formatList(record.data.Badges)
		if labels == "—" {
			labels = ""
		}
		if badges == "—" {
			badges = ""
		}

		updated := "—"
		if record.data.UpdatedAt != nil && strings.TrimSpace(*record.data.UpdatedAt) != "" {
			updated = *record.data.UpdatedAt
		}

		link := path.Join("../tasks", filepath.Base(record.sourcePath))
		fmt.Fprintf(
			builder,
			"| [%s](%s) | %s | %s | %s | %s | %s | %s |\n",
			record.data.ID,
			link,
			record.data.Title,
			record.data.Status,
			owner,
			labels,
			badges,
			updated,
		)
	}

	outputPath := filepath.Join(g.componentsDir, "issues", "tasks-table.md")
	if err := g.writeFile(outputPath, builder.String()); err != nil {
		return fmt.Errorf("write tasks table: %w", err)
	}

	return nil
}

func (g *Generator) generateTaskStatusSummary(ctx context.Context, records []recordWithPath[taskRecord]) error {
	select {
	case <-ctx.Done():
		return ctx.Err()
	default:
	}

	counts := map[string]int{}
	for _, record := range records {
		status := strings.TrimSpace(record.data.Status)
		if status == "" {
			status = "UNKNOWN"
		}
		counts[status]++
	}

	statuses := make([]string, 0, len(counts))
	for status := range counts {
		statuses = append(statuses, status)
	}

	sort.Slice(statuses, func(i, j int) bool {
		ri := statusRank(statuses[i])
		rj := statusRank(statuses[j])
		if ri != rj {
			return ri < rj
		}
		return strings.ToLower(statuses[i]) < strings.ToLower(statuses[j])
	})

	builder := &strings.Builder{}
	builder.WriteString(generatedNotice)
	builder.WriteString("\n")
	builder.WriteString("| Status | Count |\n")
	builder.WriteString("| --- | --- |\n")

	for _, status := range statuses {
		fmt.Fprintf(builder, "| %s | %d |\n", status, counts[status])
	}

	outputPath := filepath.Join(g.componentsDir, "issues", "status-summary.md")
	if err := g.writeFile(outputPath, builder.String()); err != nil {
		return fmt.Errorf("write task status summary: %w", err)
	}

	return nil
}

